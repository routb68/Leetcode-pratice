class Solution {
public:
    bool haveConflict(vector<string>& e1, vector<string>& e2) {
        int a1=0,a2=0,b1=0,b2=0;
        a1+=(((e1[0][0]-'0')*10 + (e1[0][1]-'0'))*60+ 
            ((e1[0][3]-'0')*10 + (e1[0][4]-'0')));
        a2+=(((e1[1][0]-'0')*10 + (e1[1][1]-'0'))*60+ 
            ((e1[1][3]-'0')*10 + (e1[1][4]-'0')));
        b1+=(((e2[0][0]-'0')*10 + (e2[0][1]-'0'))*60+ 
            ((e2[0][3]-'0')*10 + (e2[0][4]-'0')));
        b2+=(((e2[1][0]-'0')*10 + (e2[1][1]-'0'))*60+ 
            ((e2[1][3]-'0')*10 + (e2[1][4]-'0')));
        bool ok =false;
        if(a1<=b1 and a2>=b1) ok =true;
        if(b1<=a1 and b2>=a1) ok =true;
        return ok;
    }
};